int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (i < length - 1 && k <= 0)
	{
		k = f(tab[i], tab[i + 1]);
		i++;
	}
	k = (k <= 0) ? 1 : 0;
	return (k);
}
